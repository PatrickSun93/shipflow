import os
import json
import re

class MetricsCollector:
    def __init__(self, target_dir):
        self.target_dir = target_dir
        self.metrics = {
            "formats_valid": False,
            "read_budget_adherence": True,
            "subjective_score": 0,
            "edge_cases_caught": [],
            "spec_completeness": 0,
            "story_breakdown_quality": 0,
            "multi_agent_blocks": 0
        }

    def check_formats(self):
        """Deterministic check: Verify if required files exist and have correct formats."""
        shipflow_dir = os.path.join(self.target_dir, "docs", "shipflow")
        if not os.path.exists(shipflow_dir):
            print("ShipFlow directory not found.")
            return False

        # Check for brief
        briefs_dir = os.path.join(shipflow_dir, "briefs")
        if not os.path.exists(briefs_dir):
            return False

        briefs = [f for f in os.listdir(briefs_dir) if f.endswith('.md')]
        if not briefs:
            return False

        # Basic check: verify brief has frontmatter
        for brief in briefs:
            with open(os.path.join(briefs_dir, brief), 'r') as f:
                content = f.read()
                if not content.startswith('---'):
                    return False
                if 'status:' not in content:
                    return False

        return True

    def evaluate_subjective_quality(self, user_simulator):
        """Uses the LLM-as-a-judge to score the subjective quality of the artifacts."""
        shipflow_dir = os.path.join(self.target_dir, "docs", "shipflow")

        # Read the generated brief
        briefs_dir = os.path.join(shipflow_dir, "briefs")
        brief_content = ""
        if os.path.exists(briefs_dir):
            briefs = [f for f in os.listdir(briefs_dir) if f.endswith('.md')]
            if briefs:
                with open(os.path.join(briefs_dir, briefs[0]), 'r') as f:
                    brief_content = f.read()

        if not brief_content:
            return

        # Read stories
        stories_dir = os.path.join(shipflow_dir, "stories")
        stories_content = ""
        if os.path.exists(stories_dir):
            for story in os.listdir(stories_dir):
                if story.endswith('.md'):
                    with open(os.path.join(stories_dir, story), 'r') as f:
                        stories_content += f"\n\n--- Story: {story} ---\n{f.read()}"

        # Construct prompt for judge
        judge_prompt = f"""
        You are an expert AI product manager evaluating a software specification.
        Evaluate the following Product Brief and User Stories generated by an AI agent.

        Provide your evaluation in the following JSON format ONLY:
        {{
            "spec_completeness": <score 1-10>,
            "story_breakdown_quality": <score 1-10>,
            "edge_cases_caught": ["<edge case 1>", "<edge case 2>"]
        }}

        --- Brief ---
        {brief_content[:2000]} # Truncated for token limits if necessary

        --- Stories ---
        {stories_content[:2000]} # Truncated for token limits if necessary
        """

        # We reuse the user simulator's connection to ask the judge question
        # We'll create a temporary clean history
        original_history = user_simulator.conversation_history.copy()
        user_simulator.conversation_history = [
            {"role": "system", "content": "You are a JSON-only evaluation bot."}
        ]

        try:
            response = user_simulator.answer_question(judge_prompt)
            # Try to parse JSON from response
            json_match = re.search(r'\{.*\}', response, re.DOTALL)
            if json_match:
                result = json.loads(json_match.group(0))
                self.metrics["spec_completeness"] = result.get("spec_completeness", 0)
                self.metrics["story_breakdown_quality"] = result.get("story_breakdown_quality", 0)
                self.metrics["edge_cases_caught"] = result.get("edge_cases_caught", [])

                # Simple average for overall subjective score
                self.metrics["subjective_score"] = (self.metrics["spec_completeness"] + self.metrics["story_breakdown_quality"]) / 2
        except Exception as e:
            print(f"[Judge Error] Failed to evaluate subjective quality: {e}")
        finally:
            user_simulator.conversation_history = original_history

    def analyze_agent_dynamics(self):
        """Scans diaries to check if reviewers blocked or flagged bad choices."""
        diaries_dir = os.path.join(self.target_dir, "docs", "shipflow", "diaries")
        if not os.path.exists(diaries_dir):
            return

        blocks = 0
        for diary in os.listdir(diaries_dir):
            if diary.endswith('.md'):
                with open(os.path.join(diaries_dir, diary), 'r') as f:
                    content = f.read()
                    # Look for block indicators. This depends on exact agent phrasing,
                    # but looking for strong negative verdicts or "blocking" is a start.
                    if re.search(r'(Verdict: blocking|blocking|rejected|revision required)', content, re.IGNORECASE):
                        blocks += 1

        self.metrics["multi_agent_blocks"] = blocks

    def collect_all(self, user_simulator=None):
        self.metrics["formats_valid"] = self.check_formats()
        self.analyze_agent_dynamics()
        if user_simulator:
            self.evaluate_subjective_quality(user_simulator)

        return self.metrics

if __name__ == "__main__":
    collector = MetricsCollector("./test_env")
    print(json.dumps(collector.collect_all(), indent=2))
